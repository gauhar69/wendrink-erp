conn = sqlite3.connect(DB_PATH)
cur = conn.cursor()

ok = 0
errors = []

for pos, name, norm_stock, box_price, box_weight in DATA:
    price_per_unit = box_price / box_weight  # полная точность, не округлять

    rows = cur.execute(
        "SELECT id, name FROM ingredients WHERE position_number = ?", (pos,)
    ).fetchall()

    if not rows:
        errors.append(f"  НЕ НАЙДЕНО: позиция {pos} ({name})")
        continue

    db_name = rows[0][1]
    if db_name != name:
        errors.append(f"  ИМЯ НЕ СОВПАДАЕТ: поз.{pos} → БД='{db_name}' / таблица='{name}'")
        continue

    cur.execute("""
        UPDATE ingredients
        SET package_size   = ?,
            norm_stock     = ?,
            current_price  = ?
        WHERE position_number = ?
    """, (box_weight, norm_stock, price_per_unit, pos))
    ok += 1

conn.commit()
conn.close()

